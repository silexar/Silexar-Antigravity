#!/usr/bin/env node
/**
 * Silexar Pulse - Git Hooks Installer
 * 
 * Este script instala y configura los git hooks de seguridad
 * Se ejecuta automáticamente via npm prepare
 * 
 * Uso: node scripts/install-hooks.js
 */

import { readFileSync, writeFileSync, chmodSync, existsSync, mkdirSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, resolve, join } from 'path';
import { execSync } from 'child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT_DIR = resolve(__dirname, '..');

// Colores para terminal
const colors = {
  reset: '\x1b[0m',
  bright: '\x1b[1m',
  red: '\x1b[31m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  blue: '\x1b[34m',
  cyan: '\x1b[36m'
};

const icons = {
  check: '✅',
  cross: '❌',
  warning: '⚠️',
  info: 'ℹ️',
  gear: '⚙️',
  lock: '🔒'
};

function log(message) {
  console.log(message);
}

function success(message) {
  console.log(`${colors.green}${icons.check} ${message}${colors.reset}`);
}

function error(message) {
  console.error(`${colors.red}${icons.cross} ${message}${colors.reset}`);
}

function warning(message) {
  console.warn(`${colors.yellow}${icons.warning} ${message}${colors.reset}`);
}

function info(message) {
  console.log(`${colors.blue}${icons.info} ${message}${colors.reset}`);
}

function section(title) {
  console.log('');
  console.log(`${colors.cyan}${colors.bright}${icons.gear} ${title}${colors.reset}`);
  console.log(`${colors.cyan}${'─'.repeat(50)}${colors.reset}`);
}

/**
 * Verifica si estamos en un repositorio git
 */
function isGitRepository() {
  try {
    execSync('git rev-parse --git-dir', { cwd: ROOT_DIR, stdio: 'ignore' });
    return true;
  } catch {
    return false;
  }
}

/**
 * Verifica el sistema operativo
 */
function getPlatform() {
  return process.platform;
}

/**
 * Detecta la shell disponible en Windows
 */
function detectWindowsShell() {
  // Verificar Git Bash
  try {
    execSync('where git', { stdio: 'ignore' });
    return 'git-bash';
  } catch {
    // Verificar PowerShell
    try {
      execSync('where powershell', { stdio: 'ignore' });
      return 'powershell';
    } catch {
      return 'cmd';
    }
  }
}

/**
 * Crea el directorio .husky/_ si no existe
 */
function ensureHuskyDirectory() {
  const huskyDir = join(ROOT_DIR, '.husky');
  const huskyInternalDir = join(huskyDir, '_');
  
  if (!existsSync(huskyDir)) {
    mkdirSync(huskyDir, { recursive: true });
    info(`Creado directorio: ${huskyDir}`);
  }
  
  if (!existsSync(huskyInternalDir)) {
    mkdirSync(huskyInternalDir, { recursive: true });
    info(`Creado directorio: ${huskyInternalDir}`);
  }
  
  return huskyDir;
}

/**
 * Crea el archivo husky.sh necesario para los hooks
 */
function createHuskySh(huskyDir) {
  const huskyShPath = join(huskyDir, '_', 'husky.sh');
  
  const huskyShContent = `#!/usr/bin/env sh
# Generated by Silexar Pulse Git Hooks Installer
# This file is necessary for husky hooks to work

if [ -z "\$husky_skip_init" ]; then
  debug () {
    if [ "\$HUSKY_DEBUG" = "1" ]; then
      echo "husky (debug) - \$1"
    fi
  }

  readonly hook_name=\$(basename "\$0")
  debug "starting \$hook_name..."

  if [ "\$HUSKY" = "0" ]; then
    debug "HUSKY env variable is set to 0, skipping hook"
    exit 0
  fi

  if [ -f ~/.huskyrc ]; then
    debug "sourcing ~/.huskyrc"
    . ~/.huskyrc
  fi

  readonly husky_skip_init=1
  export husky_skip_init

  sh -e "\$0" \$@
  exitCode="\$?"

  if [ \$exitCode != 0 ]; then
    echo "husky - \$hook_name hook exited with code \$exitCode (error)"
  fi

  if [ \$exitCode = 127 ]; then
    echo "husky - command not found in PATH=\$PATH"
  fi

  exit \$exitCode
fi
`;

  writeFileSync(huskyShPath, huskyShContent, 'utf8');
  
  // Hacer ejecutable en Unix
  if (getPlatform() !== 'win32') {
    chmodSync(huskyShPath, 0o755);
  }
  
  success(`Creado: ${huskyShPath}`);
}

/**
 * Verifica y configura los hooks existentes
 */
function verifyHooks(huskyDir) {
  const hooks = ['pre-commit', 'commit-msg', 'pre-push'];
  
  section('Verificando Git Hooks');
  
  for (const hook of hooks) {
    const hookPath = join(huskyDir, hook);
    
    if (existsSync(hookPath)) {
      // Verificar si es ejecutable (solo en Unix)
      if (getPlatform() !== 'win32') {
        try {
          chmodSync(hookPath, 0o755);
          success(`Hook '${hook}' verificado y configurado`);
        } catch (err) {
          warning(`No se pudo hacer ejecutable: ${hook}`);
        }
      } else {
        success(`Hook '${hook}' verificado`);
      }
    } else {
      warning(`Hook '${hook}' no encontrado`);
    }
  }
}

