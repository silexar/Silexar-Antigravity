/**
 * Actualiza .gitignore para excluir archivos sensibles
 */
function updateGitignore() {
  section('Verificando .gitignore');
  
  const gitignorePath = join(ROOT_DIR, '.gitignore');
  let content = '';
  
  if (existsSync(gitignorePath)) {
    content = readFileSync(gitignorePath, 'utf8');
  }
  
  const requiredEntries = [
    '# Security - Credentials',
    '*.pem',
    '*.key',
    '*.p12',
    '*.pfx',
    '.env.local',
    '.env.production',
    '.env.development.local',
    'credentials.json',
    'serviceAccountKey.json',
    '.htpasswd',
    '.netrc',
    'id_rsa',
    'id_dsa',
    'id_ecdsa',
    'id_ed25519',
    '*.log',
    '*.dump',
    '# End Security'
  ];
  
  let updated = false;
  
  for (const entry of requiredEntries) {
    if (!content.includes(entry)) {
      content += '\n' + entry;
      updated = true;
    }
  }
  
  if (updated) {
    writeFileSync(gitignorePath, content, 'utf8');
    success('.gitignore actualizado con entradas de seguridad');
  } else {
    info('.gitignore ya contiene las entradas de seguridad necesarias');
  }
}

