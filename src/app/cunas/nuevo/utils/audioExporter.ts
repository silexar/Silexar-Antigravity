export interface ExportMetadata {
  title: string;
  artist: string;
  album: string;
  year: number;
  comment: string;
  silexarId: string;
  createdBy: string;
  exportDate: string;
}

export interface ExportSettings {
  format: 'wav' | 'mp3';
  bitrate: number;
  sampleRate: number;
  channels: 'mono' | 'stereo';
  metadata: ExportMetadata;
}

export interface ExportResult {
  blob: Blob;
  filename: string;
  url: string;
  metadata: ExportMetadata;
  fileSize: number;
  fileSizeFormatted: string;
}

