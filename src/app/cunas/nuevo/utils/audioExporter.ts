/**
 * Sanitiza un string para usarlo en nombre de archivo
 */
export function sanitizeFilename(name: string): string {
  return name
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '') // Quitar acentos
    .replace(/[^a-zA-Z0-9\s-_]/g, '') // Solo caracteres seguros
    .replace(/\s+/g, '_') // Espacios a guiones bajos
    .replace(/_+/g, '_') // Múltiples guiones a uno
    .substring(0, 30) // Limitar longitud
    .trim();
}

/**
 * Formatea bytes a string legible
 */
export function formatFileSize(bytes: number): string {
  if (bytes === 0) return '0 Bytes';
  const k = 1024;
  const sizes = ['Bytes', 'KB', 'MB', 'GB'];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i];
}

/**
 * Genera timestamp formateado
 */
export function generateTimestamp(): string {
  const now = new Date();
  const year = now.getFullYear();
  const month = String(now.getMonth() + 1).padStart(2, '0');
  const day = String(now.getDate()).padStart(2, '0');
  const hour = String(now.getHours()).padStart(2, '0');
  const minute = String(now.getMinutes()).padStart(2, '0');
  return `${year}${month}${day}_${hour}${minute}`;
}

/**
 * Genera nombre de archivo inteligente
 */
export function generateSmartFilename(
  cunaId: string,
  cunaName: string,
  format: string = 'wav'
): string {
  const cleanId = cunaId.toUpperCase();
  const cleanName = sanitizeFilename(cunaName);
  const timestamp = generateTimestamp();
  return `${cleanId}_${cleanName}_${timestamp}.${format}`;
}

